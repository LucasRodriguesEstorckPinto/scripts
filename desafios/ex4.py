x= input('digite qualquer coisa=')

print('Você digitou:',x)

print('O que você escreveu é do tipo:',type(x))

print('É um número?:',x.isnumeric())

print('É alfanumérico?:', x.isalnum())

print('É alfabético?:', x.isalpha())

print('Esta maiusculo?:',x.isupper())

print('Está minúsculo?:',x.islower())

print('Pode ser impresso?:',x.isprintable())

print('É um dígito?;', x.isdigit())

print('Fim das análises')